import { FileWithRelations } from '../files.type';

export class GetFileByFileId {
    id: number;
    url: string;
    userId: number;
    createdAt: Date;
    mimetype: string;
    size: number;

    constructor(file: FileWithRelations) {
        this.id = file.id;
        this.url = file.url;
        this.userId = file.userId;
        this.createdAt = file.createdAt;
        this.mimetype = file.mimetype;
        this.size = file.size;
    }
}
